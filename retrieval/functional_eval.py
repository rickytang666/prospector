from __future__ import annotations

from dataclasses import dataclass
from typing import Any

from retrieval.mock_data import get_mock_team_context
from retrieval.ranking import rank_candidates

# python3 -m retrieval.functional_eval


@dataclass
class EvalCase:
    query: str
    must_have_in_top3: list[str]
    prefer_top1_any: list[str]
    note: str = ""


CASES = [
    EvalCase(
        query="Who can help with our ground station mapping subsystem?",
        must_have_in_top3=["provider", "professor", "company"],
        prefer_top1_any=["provider"],
        note="golden path mapping",
    ),
    EvalCase(
        query="We need cloud computing credits for satellite simulation",
        must_have_in_top3=["provider"],
        prefer_top1_any=["provider"],
        note="cloud credits",
    ),
    EvalCase(
        query="Looking for a professor who can advise on embedded systems firmware",
        must_have_in_top3=["professor"],
        prefer_top1_any=["professor"],
        note="faculty guidance",
    ),
    EvalCase(
        query="We need someone to sponsor our PCB manufacturing",
        must_have_in_top3=["provider", "company"],
        prefer_top1_any=["provider", "company"],
        note="hardware sponsorship",
    ),
]


def _top_types(cands: list[Any], n: int):
    return [c.entity_type for c in cands[:n]]


def _top_ids(cands: list[Any], n: int):
    return [c.entity_id for c in cands[:n]]


def _has_type(types: list[str], wanted: str):
    return any(t == wanted for t in types)


def _score_case(out, case: EvalCase):
    cands = out.candidates
    top3_types = _top_types(cands, 3)
    top1_types = _top_types(cands, 1)

    must_hits = 0
    for t in case.must_have_in_top3:
        if _has_type(top3_types, t):
            must_hits += 1

    prefer_hit = 0
    for t in case.prefer_top1_any:
        if _has_type(top1_types, t):
            prefer_hit = 1
            break

    diversity = len(set(top3_types))
    score = (must_hits / max(1, len(case.must_have_in_top3))) * 0.7 + (prefer_hit * 0.3)
    return {
        "score": round(score, 3),
        "must_hits": must_hits,
        "must_total": len(case.must_have_in_top3),
        "prefer_hit": prefer_hit,
        "top3_types": top3_types,
        "top3_ids": _top_ids(cands, 3),
        "diversity_top3": diversity,
    }


def run_eval():
    ctx = get_mock_team_context()
    total = 0.0
    print("functional eval start")
    print("-" * 60)
    for i, case in enumerate(CASES, start=1):
        out = rank_candidates(ctx, case.query, k=5)
        res = _score_case(out, case)
        total += res["score"]

        print(f"{i}. {case.query}")
        print(f"   note={case.note}")
        print(f"   mode={out.retrieval_metadata.get('mode')} src={out.retrieval_metadata.get('candidate_source')} conf={out.retrieval_metadata.get('confidence')}")
        print(f"   top3_ids={res['top3_ids']}")
        print(f"   top3_types={res['top3_types']} diversity={res['diversity_top3']}")
        print(f"   must={res['must_hits']}/{res['must_total']} prefer_top1={res['prefer_hit']} case_score={res['score']}")
        if out.candidates:
            c0 = out.candidates[0]
            print(f"   top1={c0.name} [{c0.entity_type}] score={c0.overall_score}")
            if c0.matched_reasons:
                print(f"   top1_why={c0.matched_reasons[0]}")
        print("-" * 60)

    avg = round(total / max(1, len(CASES)), 3)
    print(f"avg_case_score={avg}")
    if avg >= 0.8:
        print("verdict=strong_demo_quality")
    elif avg >= 0.65:
        print("verdict=good_enough_demo")
    else:
        print("verdict=needs_weight_tuning")


if __name__ == "__main__":
    run_eval()

