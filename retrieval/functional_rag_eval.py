from retrieval.mock_data import get_mock_team_context
from retrieval.api import retrieve_context_pack_dict

QUERIES = [
    "Who can help with our ground station mapping subsystem?",
    "Looking for a professor who can advise on embedded systems firmware",
    "We need someone to sponsor our PCB manufacturing",
]

def run():
    ctx = get_mock_team_context()
    for i, q in enumerate(QUERIES, start=1):
        out = retrieve_context_pack_dict(
            team_context=ctx,
            query=q,
            k_entities=5,
            k_chunks=5,
        )

        em = out.get("entity_matches", [])
        ch = out.get("internal_chunks", [])
        meta = out.get("retrieval_meta", {})
        emeta = meta.get("entity", {})
        cmeta = meta.get("chunks", {})

        print("-" * 70)
        print(f"{i}. {q}")
        print(f"entity_source={emeta.get('candidate_source')} conf={emeta.get('confidence')} returned={len(em)}")
        print(f"chunk_status={cmeta.get('status')} kept={cmeta.get('kept_row_count')} error={cmeta.get('error')}")
        print("top_entities=", [f"{x.get('name')}[{x.get('entity_type')}]" for x in em[:3]])
        print("top_chunks=", [f"{x.get('source')}:{(x.get('text') or '')[:70]}" for x in ch[:3]])

if __name__ == "__main__":
    run()

